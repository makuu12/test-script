# ------------------ PYTHON AS A CALCULATOR ------------------ #

print(1+1) # Output: 2 (Addition)
print(1-1) # Output: 0 (Subtraction)
print(1*1) # Output: 1 (Multiplication)
print(1/1) # Output: 1.0 (Division)
print(1//1) # Output: 1 (Floor Division)
print(1%1) # Output: 0 (Modulus)
print(2**3) # Output: 8 (Exponentiation)

# You can also use FLoat. It is also applicable to minus
print(-4 + 4)
print(-4. + 8) 

# ------------------ OPERATORS AND THEIR PRIORITIES ------------------ #

# PEMDAS/BODMAS
print(2 + 3 * 5) # = 2 + (3*5)

print(9 % 6 % 2) # Process: 9 % 6 = 3 > 3 % 2 = 1 (From left to right)

# exponentiation
print(2 ** 2 ** 3)
# 2 ** 2 → 4; 4 ** 3 → 64
# 2 ** 3 → 8; 2 ** 8 → 256

